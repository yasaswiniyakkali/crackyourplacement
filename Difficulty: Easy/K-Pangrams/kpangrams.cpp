class Solution {
  public:

    bool kPangram(string str, int k) {
        // code here
        int cnt=0;
        unordered_map<char,int>mp;
        for(auto&x:str){
            if(x!=32){
                cnt++;
                mp[x]++;}
            }
        return cnt>=26 && 26-mp.size()<=k;
    }
    
};

